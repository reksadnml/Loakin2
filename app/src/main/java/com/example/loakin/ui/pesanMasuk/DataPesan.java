package com.example.loakin.ui.pesanMasuk;

public class DataPesan {
    public String keteranganPesan;
    public String waktuPesan;
    public int foto;

    public String getKeteranganPesan() {
        return keteranganPesan;
    }

    public void setKeteranganPesan(String keteranganPesan) {
        this.keteranganPesan = keteranganPesan;
    }

    public String getWaktuPesan() {
        return waktuPesan;
    }

    public void setWaktuPesan(String waktuPesan) {
        this.waktuPesan = waktuPesan;
    }

    public int getFoto() {
        return foto;
    }

    public void setFoto(int foto) {
        this.foto = foto;
    }
}
